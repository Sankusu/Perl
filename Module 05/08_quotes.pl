# Single and Double Quotes
print("Line with Double Quotes\n");			# With Double Quotes
print('Line with Single Quotes\n');			# With Single Quotes
# Important Note: Only Double Quotes interpolates variables and special characters

# Examples
$a = 10;
print("\nValue of a =  $a\n");
print('\nValue of a = $a\n');

# Ouput:
# Line with Double Quotes
# Line with Single Quotes\n
# Value of a =  10
# \nValue of a = $a\n$