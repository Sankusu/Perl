#!/usr/local/bin/perl

# A while loop statement repeatedly executes as long as the condition is true.

# Syntax:
# while(condition) {
#    statement(s);
# }

$a = 11;

# while loop execution
while($a<=20) {
	printf "Value of a: $a\n";
	$a = $a + 1;
}


# Output:
# Value of a: 11
# Value of a: 12
# Value of a: 13
# Value of a: 14
# Value of a: 15
# Value of a: 16
# Value of a: 17
# Value of a: 18
